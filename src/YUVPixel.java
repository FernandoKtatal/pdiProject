
public class YUVPixel {
	
	double y;
	double u;
	double v;
	
	public YUVPixel(double y, double u, double v){
		this.y = y;
		this.u = u;
		this.v = v;
	}
	
	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

	public double getU() {
		return u;
	}

	public void setU(double u) {
		this.u = u;
	}

	public double getV() {
		return v;
	}

	public void setV(double v) {
		this.v = v;
	}

}
